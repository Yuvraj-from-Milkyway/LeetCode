class Solution {
  public:
    void printTriangle(int n) {
        char i, j;
        for(i = 'A'; i <= ('A' + n-1); i++){
            for(j = 'A'; j <= i; j++){
              cout << i;
            }
            cout << endl;
        }
    }
};

//{ Driver Code Starts.

int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;

        Solution ob;
        ob.printTriangle(n);
    }
    return 0;
}
