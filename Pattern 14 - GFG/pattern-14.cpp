class Solution {
  public:
    void printTriangle(int n) {
        int i,j;
        char c;
        for(i=1;i<=n;i++){
            for(j=65;j<(65+i);j++){
              c = j;
              cout << c;
            }
            cout << endl;
        }
    }
};

